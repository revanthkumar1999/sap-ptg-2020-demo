package labAssignments;

public class DemoArray {
		 public static void main(String[] args) {
		  
		   // create an array
		   int[] num = {3, 4, 5};

		   // loop through the array
		   // using for loop
		   System.out.println("Using for Loop:");
		   for(int i = 0; i < num.length; i++) {
		     System.out.println(num[i]);
		     
		   }
		 }
}
